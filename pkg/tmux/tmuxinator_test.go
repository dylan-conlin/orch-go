func TestTmuxinatorConfig_Write(t *testing.T) {
	// Use a temporary directory
	tmpDir := t.TempDir()

	// Override the config dir for testing
	oldHome := os.Getenv("HOME")
	os.Setenv("HOME", tmpDir)
	defer os.Setenv("HOME", oldHome)

	config := &TmuxinatorConfig{
		Name:       "workers-test",
		Root:       "/path/to/test",
		Project:    "test",
		ServerPane: []ServerPane{},
	}

	path, err := config.Write()
	if err != nil {
		t.Fatalf("Write() error = %v", err)
	}

	// Check file was created
	expectedPath := filepath.Join(tmpDir, ".tmuxinator", "workers-test.yml")
	if path != expectedPath {
		t.Errorf("Write() path = %q, want %q", path, expectedPath)
	}

	// Check file exists and has content
	content, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("Failed to read written file: %v", err)
	}

	if !strings.Contains(string(content), "name: workers-test") {
		t.Error("Written file missing expected content")
	}
}

