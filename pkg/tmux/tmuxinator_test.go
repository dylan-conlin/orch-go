func TestGenerateTmuxinatorConfig_WithPorts(t *testing.T) {
	// Create a temporary port registry
	tmpDir := t.TempDir()
	registryPath := filepath.Join(tmpDir, "ports.yaml")

	reg, err := port.New(registryPath)
	if err != nil {
		t.Fatalf("Failed to create port registry: %v", err)
	}

	// Allocate some ports
	_, err = reg.Allocate("snap", "web", port.PurposeVite)
	if err != nil {
		t.Fatalf("Failed to allocate vite port: %v", err)
	}
	_, err = reg.Allocate("snap", "api", port.PurposeAPI)
	if err != nil {
		t.Fatalf("Failed to allocate api port: %v", err)
	}

	// Generate config
	config, err := GenerateTmuxinatorConfig("snap", "/path/to/snap", reg)
	if err != nil {
		t.Fatalf("GenerateTmuxinatorConfig() error = %v", err)
	}

	if config.Name != "workers-snap" {
		t.Errorf("config.Name = %q, want %q", config.Name, "workers-snap")
	}
	if len(config.ServerPane) != 2 {
		t.Errorf("config.ServerPane length = %d, want 2", len(config.ServerPane))
	}

	// Check that panes have correct ports
	for _, pane := range config.ServerPane {
		if pane.Service == "web" && pane.Port != 5173 {
			t.Errorf("web service port = %d, want 5173", pane.Port)
		}
		if pane.Service == "api" && pane.Port != 3333 {
			t.Errorf("api service port = %d, want 3333", pane.Port)
		}
	}
}

func TestTmuxinatorConfig_Render_NoPorts(t *testing.T) {
	config := &TmuxinatorConfig{
		Name:       "workers-snap",
		Root:       "/path/to/snap",
		Project:    "snap",
		ServerPane: []ServerPane{},
	}

	rendered, err := config.Render()
	if err != nil {
		t.Fatalf("Render() error = %v", err)
	}

	// Check key elements
	expectedParts := []string{
		"# Auto-generated by orch spawn",
		"name: workers-snap",
		"root: /path/to/snap",
		"startup_window: servers",
		"- servers:",
		"# Dev servers (npm run dev, etc.)",
	}

	for _, part := range expectedParts {
		if !strings.Contains(rendered, part) {
			t.Errorf("Render() output missing: %q", part)
		}
	}

	// Should NOT have layout: even-horizontal with no panes
	if strings.Contains(rendered, "layout: even-horizontal") {
		t.Error("Render() should not include layout: even-horizontal with no panes")
	}
}

func TestTmuxinatorConfig_Render_WithPorts(t *testing.T) {
	config := &TmuxinatorConfig{
		Name:    "workers-snap",
		Root:    "/path/to/snap",
		Project: "snap",
		ServerPane: []ServerPane{
			{Command: "bun run dev --port 5173", Service: "web", Port: 5173},
			{Command: "# api server on port 3333", Service: "api", Port: 3333},
		},
	}

	rendered, err := config.Render()
	if err != nil {
		t.Fatalf("Render() error = %v", err)
	}

	// Check key elements
	expectedParts := []string{
		"# Auto-generated by orch spawn",
		"name: workers-snap",
		"root: /path/to/snap",
		"layout: even-horizontal", // Should have layout with 2+ panes
		"- bun run dev --port 5173",
		"- # api server on port 3333",
	}

	for _, part := range expectedParts {
		if !strings.Contains(rendered, part) {
			t.Errorf("Render() output missing: %q\nGot:\n%s", part, rendered)
		}
	}

	// Should NOT have the default placeholder comment
	if strings.Contains(rendered, "# Dev servers (npm run dev, etc.)") {
		t.Error("Render() should not include default comment when panes are defined")
	}
}

