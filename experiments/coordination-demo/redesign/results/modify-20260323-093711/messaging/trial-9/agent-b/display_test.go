func TestTruncate(t *testing.T) {
	tests := []struct {
		input  string
		maxLen int
		want   string
	}{
		// ASCII cases (existing behavior)
		{"hello", 10, "hello"},
		{"hello", 5, "hello"},
		{"hello world", 8, "hello..."},
		{"abcdefghij", 6, "abc..."},
		{"", 5, ""},
		// Unicode cases (multi-byte UTF-8)
		{"café", 10, "café"},                    // Fits within limit
		{"café", 4, "café"},                     // Exactly at limit
		{"café café", 6, "caf..."},              // Accented chars truncation
		{"hello 世界", 7, "hell..."},            // Mixed ASCII and CJK
		{"你好世界天", 4, "你..."},              // CJK characters truncate to 4 runes (1+1+3)
		{"你好世界天下", 4, "你..."},            // Multiple CJK truncation
	}
	for _, tt := range tests {
		got := Truncate(tt.input, tt.maxLen)
		if got != tt.want {
			t.Errorf("Truncate(%q, %d) = %q, want %q", tt.input, tt.maxLen, got, tt.want)
		}
		// Verify rune count doesn't exceed maxLen
		if len([]rune(got)) > tt.maxLen {
			t.Errorf("Truncate(%q, %d) returned %d runes, want <= %d", tt.input, tt.maxLen, len([]rune(got)), tt.maxLen)
		}
	}
}

func TestTruncateWithPadding(t *testing.T) {
	tests := []struct {
		input  string
		maxLen int
		want   string
	}{
		// ASCII cases (existing behavior)
		{"hello", 10, "hello     "},
		{"hello world", 8, "hello..."},
		{"exact", 5, "exact"},
		// Unicode cases (multi-byte UTF-8)
		{"café", 6, "café  "},                     // Pad with spaces
		{"café", 4, "café"},                       // Exactly at limit
		{"café café café", 8, "café ..."},         // Accented chars with truncation
		{"你好", 4, "你好  "},                      // CJK with padding
		{"你好世界天", 4, "你..."},                 // CJK truncation
		{"test 日本 lang", 8, "test ..."},         // Mixed script truncation
	}
	for _, tt := range tests {
		got := TruncateWithPadding(tt.input, tt.maxLen)
		if got != tt.want {
			t.Errorf("TruncateWithPadding(%q, %d) = %q, want %q", tt.input, tt.maxLen, got, tt.want)
		}
		// Verify output is exactly maxLen runes
		gotRunes := len([]rune(got))
		if gotRunes != tt.maxLen {
			t.Errorf("TruncateWithPadding(%q, %d) returned %d runes, want %d", tt.input, tt.maxLen, gotRunes, tt.maxLen)
		}
	}
}

